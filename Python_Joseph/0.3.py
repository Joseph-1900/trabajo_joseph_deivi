velocidad = float(input("Digite la velocidad en kilometros por horas: "))

tiempo_hora = float(input("Digite el tiempo en horas: "))

distancia_kilometros = velocidad * tiempo_hora

print(f"La distancia que usted recorrio es de: {distancia_kilometros}")

